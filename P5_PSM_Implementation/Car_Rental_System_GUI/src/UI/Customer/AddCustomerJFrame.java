    private void BtnSaveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BtnSaveActionPerformed
        try (Connection conn = DatabaseConnection.getConnection()) {
            // Fetch and validate values
            String firstName = TxtFirstName.getText();
            String lastName = TxtLastName.getText();
            String dobText = TxtdateOfBirth.getText();
            String licenseExpiryText = TxtLED.getText();

            // Validate gender
            if (ComboGender.getSelectedIndex() == 0) {
                JOptionPane.showMessageDialog(this, "Please select a valid gender.");
                return;
            }
            String gender = ComboGender.getSelectedItem().toString();

            // Validate dates
            java.sql.Date dob, licenseExpiryDate;
            try {
                dob = java.sql.Date.valueOf(dobText);
                licenseExpiryDate = java.sql.Date.valueOf(licenseExpiryText);
            } catch (IllegalArgumentException e) {
                JOptionPane.showMessageDialog(this, "Please enter valid dates in the format YYYY-MM-DD.");
                return;
            }

            // Other inputs
            String phoneNumber = TxtPhoneNumber.getText();
            String email = TxtEmail.getText();
            String nationality = TxtNationality.getText();

            // Validate address
            if (ComboAddress.getSelectedIndex() == 0) {
                JOptionPane.showMessageDialog(this, "Please select a valid address.");
                return;
            }
            int addressID = Integer.parseInt(ComboAddress.getSelectedItem().toString().split(" - ")[0]);

            // Validate payment method
            if (ComboPreferredPM.getSelectedIndex() == 0) {
                JOptionPane.showMessageDialog(this, "Please select a preferred payment method.");
                return;
            }
            String preferredPaymentMethod = ComboPreferredPM.getSelectedItem().toString();

            String driverLicenseNumber = TxtDLN.getText();

            // Insert into Person
            String insertPersonSQL = "INSERT INTO Person (Person_Type, FirstName, LastName, DateOfBirth, Gender, PhoneNumber, Email, Nationality, AddressID) VALUES (?, ?, ?, ?, ?, ?, ?, ?, ?)";
            PreparedStatement personStmt = conn.prepareStatement(insertPersonSQL, Statement.RETURN_GENERATED_KEYS);
            personStmt.setString(1, "CUS");
            personStmt.setString(2, firstName);
            personStmt.setString(3, lastName);
            personStmt.setDate(4, dob);
            personStmt.setString(5, gender);
            personStmt.setString(6, phoneNumber);
            personStmt.setString(7, email);
            personStmt.setString(8, nationality);
            personStmt.setInt(9, addressID);
            personStmt.executeUpdate();

            ResultSet rs = personStmt.getGeneratedKeys();
            int personID = rs.next() ? rs.getInt(1) : 0;

            // Insert into Customer
            String insertCustomerSQL = "INSERT INTO Customer (Customer_PersonID, LicenseExpiryDate, PreferredPaymentMethod, DriverLicenseNumber) VALUES (?, ?, ?, ?)";
            PreparedStatement customerStmt = conn.prepareStatement(insertCustomerSQL);
            customerStmt.setInt(1, personID);
            customerStmt.setDate(2, licenseExpiryDate);
            customerStmt.setString(3, preferredPaymentMethod);
            customerStmt.setString(4, driverLicenseNumber);
            customerStmt.executeUpdate();

            JOptionPane.showMessageDialog(this, "Customer added successfully!");
            // Navigate back to ManageCustomersJFrame
            if (parent instanceof ManageCustomersJFrame) {
                ((ManageCustomersJFrame) parent).loadCustomerData();// Optional: Refresh customer table
                this.dispose(); // Close AddCustomerJFrame
                parent.setVisible(true); // Show ManageCustomersJFrame
            }

        } catch (SQLException e) {
            e.printStackTrace();
            JOptionPane.showMessageDialog(this, "Error adding customer: " + e.getMessage());
        }

        // TODO add your handling code here:
    }//GEN-LAST:event_BtnSaveActionPerformed

