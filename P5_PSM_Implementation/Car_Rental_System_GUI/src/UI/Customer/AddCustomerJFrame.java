    private void loadAddressIDs() {
        try (Connection conn = DatabaseConnection.getConnection(); Statement stmt = conn.createStatement(); ResultSet rs = stmt.executeQuery("SELECT AddressID, CONCAT(Street, ', ', City, ', ', State) AS Address FROM Address")) {

            DefaultComboBoxModel<String> model = new DefaultComboBoxModel<>();
            model.addElement("Select--"); // Default option
            while (rs.next()) {
                model.addElement(rs.getInt("AddressID") + " - " + rs.getString("Address"));
            }
            ComboAddress.setModel(model);

        } catch (SQLException e) {
            e.printStackTrace();
            JOptionPane.showMessageDialog(this, "Error loading Address IDs.");
        }
    }

